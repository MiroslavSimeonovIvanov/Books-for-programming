var enteredDigit = Number(window.prompt("Please enter a digit", ""));

switch (enteredDigit) {
  case 0:
    console.log("нула");
    break;
  case 1:
    console.log("едно");
    break;
  case 2:
    console.log("две");
    break;
  case 3:
    console.log("три");
    break;
  case 4:
    console.log("четири");
    break;
  case 5:
    console.log("пет");
    break;
  case 6:
    console.log("шест");
    break;
  case 7:
    console.log("седем");
    break;
  case 8:
    console.log("осем");
    break;
  case 9:
    console.log("девет");
    break;
  default:
    console.log("Не сте въвели цифра!");
}
